	template <class T>
	Mesh* MarchingCubes<T>::extractMesh(const SparseVoxelGrid<T>& grid)
    {
		// extract iso-surface for each voxel
        const size_t max_threads = static_cast<size_t>(omp_get_max_threads());
        std::vector< std::vector<Triangle> > results(max_threads);

		for (auto iter = grid.begin(); iter != grid.end(); iter++)
		{
            const Vec3i& pos_grid = iter->first;
            size_t thread = static_cast<size_t>(omp_get_thread_num());
            extractSurfaceAt(pos_grid, grid, results[thread]);
		}

		// determine and check number of triangles
        size_t num_triangles = 0;
		for (size_t i = 0; i < results.size(); i++)
            num_triangles += results[i].size();
        std::cout << "Mesh (original): " << num_triangles << " triangles, " << (num_triangles * 3) << " vertices" << std::endl;
        if (num_triangles == 0)
            return nullptr;

        // merge results into single mesh
        Mesh* mesh = merge(results);
        std::cout << "Mesh (unified): " << mesh->face_vertices.size() << " triangles, " << mesh->vertices.size()  << " vertices" << std::endl;

		// remove degenerate faces
        MeshUtil::removeDegenerateFaces(mesh);
        std::cout << "Mesh (cleaned): " << mesh->face_vertices.size() << " triangles, " << mesh->vertices.size() << " vertices" << std::endl;

		return mesh;
	}


    template <class T>
    Mesh* MarchingCubes<T>::merge(const std::vector< std::vector<Triangle> > &results) const
    {
        // unified vertex indices
        typedef std::tuple<float, float, float> tuple3;
        std::map<tuple3, int> vertex_indices;

        // create mesh with unified mesh vertices
        Mesh* mesh = new Mesh();
        size_t num_faces = 0;
        for (size_t i = 0; i < results.size(); i++)
        {
            for (size_t j = 0; j < results[i].size(); j++)
            {
                // add vertices for triangle
                Triangle face = results[i][j];
                Vec3i face_verts;
                for (int t = 0; t < 3; ++t)
                {
                    Vertex v;
                    if (t == 0) v = face.v0;
                    else if (t == 1) v = face.v1;
                    else v = face.v2;
                    // pos
                    Vec3f p = v.p;
                    // color
                    Vec3b c = Vec3f(v.c * 255.0f).cast<unsigned char>();
                    // lookup vertex index first
                    tuple3 tpl = std::make_tuple(p[0], p[1], p[2]);
                    if (vertex_indices.find(tpl) == vertex_indices.end())
                    {
                        // vertex does not exist yet -> store it
                        vertex_indices[tpl] = static_cast<int>(mesh->vertices.size());
                        // add vertex
                        mesh->vertices.push_back(p);
                        mesh->colors.push_back(c);
                    }
                    face_verts[t] = vertex_indices[tpl];
                }
                // add face
                mesh->face_vertices.push_back(face_verts);
                ++num_faces;
            }
        }
        return mesh;
    }


	template <class T>
    void MarchingCubes<T>::extractSurfaceAt(const Vec3i& voxel, const SparseVoxelGrid<T>& grid, std::vector<Triangle>& result)
	{
        const float iso_value = 0.0f;

        const int x = voxel[0];
        const int y = voxel[1];
        const int z = voxel[2];
		if (!grid.exists(x, y, z) || !grid.exists(x + 1, y, z) || !grid.exists(x, y + 1, z) || !grid.exists(x, y, z + 1))
			return;

		// compute configuration for voxel
        const int cubeindex = computeLutIndex(grid, x, y, z, iso_value);

		// compute triangles for voxel
        Vertex edge_verts[12];
		if (cubeindex != 0 && cubeindex != 255)
		{
			if (edge_table_[cubeindex] & 1)
			{
				// interpolate between vertices 0 and 1
                edge_verts[0] = getVertex(grid, x + 1, y + 1, z, x + 1, y, z, iso_value);
			}

			if (edge_table_[cubeindex] & 2)
			{
				// interpolate between vertices 1 and 2
                edge_verts[1] = getVertex(grid, x + 1, y, z, x, y, z, iso_value);
			}

			if (edge_table_[cubeindex] & 4)
			{
				// interpolate between vertices 2 and 3
                edge_verts[2] = getVertex(grid, x, y, z, x, y + 1, z, iso_value);
			}

			if (edge_table_[cubeindex] & 8)
			{
				// interpolate between vertices 3 and 0
                edge_verts[3] = getVertex(grid, x, y + 1, z, x + 1, y + 1, z, iso_value);
			}

			if (edge_table_[cubeindex] & 16)
			{
				// interpolate between vertices 4 and 5
                edge_verts[4] = getVertex(grid, x + 1, y + 1, z + 1, x + 1, y, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 32)
			{
				// interpolate between vertices 5 and 6
                edge_verts[5] = getVertex(grid, x + 1, y, z + 1, x, y, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 64)
			{
				// interpolate between vertices 6 and 7
                edge_verts[6] = getVertex(grid, x, y, z + 1, x, y + 1, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 128)
			{
				// interpolate between vertices 7 and 4
                edge_verts[7] = getVertex(grid, x, y + 1, z + 1, x + 1, y + 1, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 256)
			{
				// interpolate between vertices 0 and 4
                edge_verts[8] = getVertex(grid, x + 1, y + 1, z, x + 1, y + 1, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 512)
			{
				// interpolate between vertices 1 and 5
                edge_verts[9] = getVertex(grid, x + 1, y, z, x + 1, y, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 1024)
			{
				// interpolate between vertices 2 and 6
                edge_verts[10] = getVertex(grid, x, y, z, x, y, z + 1, iso_value);
			}

			if (edge_table_[cubeindex] & 2048)
			{
				// interpolate between vertices 3 and 7
                edge_verts[11] = getVertex(grid, x, y + 1, z, x, y + 1, z + 1, iso_value);
			}

			for (int i = 0; triangle_table_[cubeindex][i] != -1; i += 3)
			{
				Triangle t;
                t.v0 = edge_verts[triangle_table_[cubeindex][i + 0]];
                t.v1 = edge_verts[triangle_table_[cubeindex][i + 1]];
                t.v2 = edge_verts[triangle_table_[cubeindex][i + 2]];
				result.push_back(t);
			}
		}
	}


	// Finding which vertices are below zero.
	// Input: Integers i,j and k
	// Output: A vector with integer with value between 0 and 256.
	template <class T>
    int MarchingCubes<T>::computeLutIndex(const SparseVoxelGrid<T>& grid, int i, int j, int k, float iso_value)
	{
		const Vec3i coords[8] =
		{
			Vec3i(i + 1, j + 1, k),
			Vec3i(i + 1, j, k),
			Vec3i(i, j, k),
			Vec3i(i, j + 1, k),
			Vec3i(i + 1, j + 1, k + 1),
			Vec3i(i + 1, j, k + 1),
			Vec3i(i, j, k + 1),
			Vec3i(i, j + 1, k + 1)
		};
		
		// check if voxels are valid and not behind the surface
		for (unsigned char i = 0; i < 8; ++i)
            if (!grid.exists(coords[i]) || grid.voxel(coords[i]).weight == 0.0f)
				return 0;

		// build cube index based on sdf signs in voxels
		int cubeindex = 0;
		for (unsigned char i = 0; i < 8; ++i)
            if (grid.voxel(coords[i]).sdf < iso_value)
                cubeindex += static_cast<int>(std::pow(2.0, i));
		return cubeindex;
	}


	template <class T>
    Vec3f MarchingCubes<T>::interpolate(float tsdf0, float tsdf1, const Vec3f &val0, const Vec3f &val1, float iso_value)
	{
        if (std::fabs(iso_value - tsdf0) < 0.00001f)
			return val0;
        if (std::fabs(iso_value - tsdf1) < 0.00001f)
			return val1;
		if (std::fabs(tsdf0 - tsdf1) < 0.00001f)
			return val0;

        float mu = (iso_value - tsdf0) / (tsdf1 - tsdf0);
        mu = std::max(std::min(mu, 1.0f), 0.0f);

        Vec3f val = val0 + mu * (val1 - val0);
		return val;
	}


	template <class T>
    typename MarchingCubes<T>::Vertex MarchingCubes<T>::getVertex(const SparseVoxelGrid<T>& grid, int i1, int j1, int k1, int i2, int j2, int k2, float iso_value)
	{
        const T& vx1 = grid.voxel(i1, j1, k1);
        const float sdf1 = static_cast<float>(vx1.sdf);
        const Vec3f p1 = grid.voxelToWorld(Vec3i(i1, j1, k1));
        const T& vx2 = grid.voxel(i2, j2, k2);
        const float sdf2 = static_cast<float>(vx2.sdf);
        const Vec3f p2 = grid.voxelToWorld(Vec3i(i2, j2, k2));
        const Vec3f vert = interpolate(sdf1, sdf2, p1, p2, iso_value);

        // linear interpolation
        const Vec3f c1f = vx1.color.template cast<float>() * (1.0f / 255.0f);
        const Vec3f c2f = vx2.color.template cast<float>() * (1.0f / 255.0f);
        const Vec3f c = interpolate(sdf1, sdf2, c1f, c2f, iso_value);

		MarchingCubes<T>::Vertex v;
		v.p = vert;
		v.c = c;
		return v;
	}


